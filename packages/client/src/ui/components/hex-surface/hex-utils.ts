// ---------------------------------------------------------------------------
// Resource rolling with geographic bias
// ---------------------------------------------------------------------------

/**
 * Roll resource type for a diamond hex with geographic bias.
 * Top rows (0-2): +tree (isotopes)
 * Bottom rows (7-9): +ore (minerals)
 * Edge columns (first or last in row): +water
 * Center area: balanced / +vent
 */
export function rollResourceType(
  seed: number,
  slotId: string,
  row?: number,
  col?: number,
): ResourceType {
  const h = Math.abs(Math.sin(seed * 0.3 + hashStr(slotId) * 0.07)) * 100;

  // Parse row/col from id if not provided
  let r = row;
  let c = col;
  if (r === undefined || c === undefined) {
    const m = slotId.match(/^d(\d+)-(\d+)$/);
    if (m) {
      r = parseInt(m[1], 10);
      c = parseInt(m[2], 10);
    }
  }

  if (r !== undefined && c !== undefined) {
    const rowWidth = DIAMOND_ROWS[r] ?? 1;
    const isEdgeCol = c === 0 || c === rowWidth - 1;
    const isTopArea = r <= 2;
    const isBottomArea = r >= 7;

    // Edge columns: strong water bias
    if (isEdgeCol) {
      if (h < 50) return 'water';
      if (h < 70) return 'vent';
      if (h < 85) return 'tree';
      return 'ore';
    }
    // Top rows: tree bias
    if (isTopArea) {
      if (h < 50) return 'tree';
      if (h < 70) return 'ore';
      if (h < 85) return 'vent';
      return 'water';
    }
    // Bottom rows: ore bias
    if (isBottomArea) {
      if (h < 50) return 'ore';
      if (h < 70) return 'tree';
      if (h < 85) return 'vent';
      return 'water';
    }
  }

  // Center / default: balanced with slight vent bias
  if (h < 30) return 'tree';
  if (h < 55) return 'ore';
  if (h < 80) return 'vent';
  return 'water';
}

function hashStr(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0;
  return h;
}

// ---------------------------------------------------------------------------
// Respawn + harvest math
// ---------------------------------------------------------------------------

export const RESOURCE_RESPAWN_MS = 3_600_000; // 1 hour

/** Calculate how many units have accumulated since last harvest (capped at maxCapacity) */
export function getAccumulatedYield(
  lastHarvestedAt: number | undefined,
  yieldPerHour: number,
  maxCapacity: number,
): number {
  if (!lastHarvestedAt) return yieldPerHour; // first harvest = 1 hour worth
  const elapsedHours = (Date.now() - lastHarvestedAt) / RESOURCE_RESPAWN_MS;
  return Math.min(maxCapacity, Math.floor(elapsedHours * yieldPerHour));
}

/** Check if at least 1 unit has accumulated */
export function isResourceReady(lastHarvestedAt: number | undefined, yieldPerHour: number = 1): boolean {
  if (!lastHarvestedAt) return true;
  const elapsedMs = Date.now() - lastHarvestedAt;
  const msPerUnit = RESOURCE_RESPAWN_MS / yieldPerHour;
  return elapsedMs >= msPerUnit;
}

/** Time remaining until next unit (ms) */
export function respawnTimeRemaining(lastHarvestedAt: number, yieldPerHour: number = 1): number {
  const msPerUnit = RESOURCE_RESPAWN_MS / yieldPerHour;
  return Math.max(0, msPerUnit - (Date.now() - lastHarvestedAt));
}

